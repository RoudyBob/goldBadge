// Assignment operator
let x = 20;

// Addition assignment operator
x += 1;
console.log(x);

// Subtraction assignment operator
x -= 1;
console.log(x);

// Multiplication assignment operator
x *= 2;
console.log(x);

// Division assignment operator
x /= 2;
console.log(x);

// Remainder (modulous) assignment operator
x %= 2; 
console.log(x);

// Modulous operator
console.log(5%3); // 5 divided by 3 gives us 2 remainder
console.log(25%7); // 25 divided by 7 gives us 4 remainder

// Exponential assignment operator
x **=2;
console.log(x);

let y = 7;
y **=3;
console.log(y);
